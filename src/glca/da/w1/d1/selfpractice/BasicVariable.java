package glca.da.w1.d1.selfpractice;

/*
 * Q1) Write a java program, declare the variables of type int, float, double, 
 * char, String, boolean, initialize all the variables with sample value and 
 * print them on the console.
 */
public class BasicVariable {
	
	public static void main(String[] args) {
    
		// 1. Declare and initialize all variables
		int number1 =10;
    	float number2=20;
    	double number3 = 134.5;
    	char ch = 'A';
    	String str="Hello Learners!";
    	boolean value = true;

    	// 2. print all the variable values
    	System.out.println(number1);
    	System.out.println(number2);
    	System.out.println(number3);
    	System.out.println(ch);
    	System.out.println(str);
    	System.out.println(value);   
    }
			                    	
}       
