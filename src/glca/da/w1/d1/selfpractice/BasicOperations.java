package glca.da.w1.d1.selfpractice;

/*
 * Q2) Declare two variables of integer type (num1,num2), Initialize value 
 * to perform addition, subtraction, multiplication and division operations.  
 * Print the result of these operations.
 */
public class BasicOperations {
	
	public static void main(String[] args) {
		
//		1. Declare two variables and initialize them
		int num1=20;
    	int num2=10;
    	
//    	2. Addition
    	System.out.println("Num1 + Num2 ="+ (num1+num2));
    	
//    	3. Subtraction
    	System.out.println("Num1 - Num2 ="+ (num1-num2));
    	
//    	4. Multiplication
    	System.out.println("Num1 * Num2 ="+ (num1*num2));
    	
//    	5. Division
    	System.out.println("Num1 / Num2 ="+ (num1/num2));
                    
    }
}
